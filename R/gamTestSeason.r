              gamDiff.bl.mn       = sub.gamDiff.tmp$per.mn[1] ,
              gamDiff.cr.mn       = sub.gamDiff.tmp$per.mn[2] ,
              gamDiff.bl.mn.obs   = sub.gamDiff.tmp$per.mn.obs[1] ,
